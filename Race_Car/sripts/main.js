//animates smoke
var animateSmoke = function() {
    ctx.clearRect(0, 0, ctx.canvas.width, ctx.canvas.height);
    for (c = 0; c !== smokeNumber; c += 1) {
        ctx.beginPath();
        //resets smoke when it is outside of y of canvas
        if (smoke[c].y < 0) {
            smoke[c].y = 370;
            smoke[c].x = 600;
            smoke[c].al = smokeAl;
            smoke[c].size = smokeSize;
        }
        //randomly moves smoke upward
        if (Math.floor(Math.random() * 2) === 0) {
            smoke[c].y -= (Math.random() * 10) + 1;
        }
        //randomly moves smoke right
        if (Math.floor(Math.random() * 2) === 0) {
            smoke[c].x += (Math.random() * 10) + 1;
        }
        //smoke increases in size
        smoke[c].size += 0.4;
        //creates smoke
        ctx.arc(smoke[c].x, smoke[c].y, smoke[c].size, 0, 360);
        //smoke alpha gets lower
        if (smoke[c].al > 0.002) {
            smoke[c].al -= 0.002;
        }
        //draws smoke
        ctx.globalAlpha = smoke[c].al;
        ctx.fillStyle = "#696969";
        ctx.fill();
        ctx.closePath();
    }
    drawCar();
};

//Sets starting interval
var myInterval = setInterval(animateSmoke, (sliderMax / 2) * (sliderMax / 2));

//sets new interval when slider receives new input
slider.oninput = function() {
    clearInterval(myInterval);
    myInterval = setInterval(animateSmoke, (sliderMax - this.value) * (sliderMax - this.value));
};
